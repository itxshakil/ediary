<div class="container mx-auto px-3 md:px-6">
    <article class="bg-gray-200 rounded-lg p-4 m-2 text-gray-900">
        <h1 class="text-3xl md:text-5xl lg:text-6xl font-bold text-center pb-4">
            How to write Diary
        </h1>
        <p class="text-xl mb-1">
            Diaries are wonderful objects that allow you to discuss your emotions, record dreams or ideas, and reflect
            on
            daily life in a safe, private space. While there's no single, definitive way to write a diary, there are
            some
            basic tricks you can use to get the most out of your writing. If you aren't sure what to write about, using
            prompts like inspirational quotes can help get started on new entries.
        </p>
        <p class="text-xl mb-1">
            You can write about anything that you want in your diary, but sometimes it's hard to get started. An easy
            way to
            start a diary entry is to just write about your day. Write about your morning before work or school, what
            you
            had for lunch, your ride home, and anything else you did that day. You can also include your feelings about
            the
            things that happened and the mood you're in.
        </p>
        <h2 class="text-3xl py-2  font-semibold">
            Brainstorming Ideas
        </h2>
        <h2 class="text-3xl py-2  font-semibold">
            Write about the events of your day.
        </h2>
        <p class="text-xl mb-1">
            Think about everything that happened that day and record any highlights or feelings that stand out to you.
            Even
            if you had a pretty standard day, you might be surprised by deeper thoughts and feelings that come up as you
            write down details about your day.
            <ul class="list-disc my-1 list-inside">
                <li class="mb-1 text-lg">Feel free to veer off into any topic you want as you're writing about the day's
                    events.
                </li>
                <li class="mb-1 text-lg">For example, you could write about the English exam you took at school that
                    day. Are you feeling good
                    about the exam? Do you wish you had studied more? Are you nervous to receive your grade?
                </li>
            </ul>
        </p>
        <h2 class="text-3xl py-2  font-semibold">
            Contemplate your goals for the future and how to achieve them.
        </h2>
        <p class="text-xl mb-1">
            Make a list of your short-term and long-term goals. Then, go through each item on the list and write in
            detail
            about your plan to accomplish the goal. Breaking each goal up into smaller tasks that you can work on can
            make
            your goals feel less overwhelming.
            <ul class="list-disc my-1 list-inside">
                <li class="mb-1 text-lg">For example, you could write about short-term goals like studying for your
                    algebra exam or hitting the
                    gym for a cardio session.</li>
                <li class="mb-1 text-lg">Long-term goals would be stuff like choosing and applying to colleges or saving
                    up money to buy a car.
                </li>
            </ul>
        </p>
        <h2 class="text-3xl py-2  font-semibold">
            Jot down your current feelings or mood.
        </h2>
        <p class="text-xl mb-1">
            Don't worry about providing any context for your emotions, just focus on accurately describing what they
            are.
            You can then use those feelings and thoughts as prompts for creating detailed diary entries. Work on one
            thought
            or emotion at a time and explore it as fully as you can.
            <ul class="list-disc my-1 list-inside">
                <li class="mb-1 text-lg">For example, if you're feeling sad, you can write a diary entry about why you
                    feel that way and any
                    events that may have contributed.
                </li>
            </ul>
        </p>
        <h2 class="text-3xl py-2  font-semibold">
            Write down inspirational quotes and what they mean to you.
        </h2>
        <p class="text-xl mb-1">
            Inspirational quotes can come from anywhere--a famous person, your favourite book or movie, or even a friend
            or
            a family member. Any quote you find inspiring is a great starting point. Record the quote in your diary and
            note
            where it came from. Then, explain what it means to you in your own words.
            <ul class="list-disc my-1 list-inside">
                <li class="mb-1 text-lg">For example, you might write down a quote like, "The secret of getting ahead is
                    getting started," which
                    came from Mark Twain. Write a diary entry about what this means to you and what things you need to
                    get
                    started on to achieve certain goals.
                </li>
            </ul>
        </p>
        <h2 class="text-3xl py-2  font-semibold">
            Explore your favorite subjects or hobbies in-depth.
        </h2>
        <p class="text-xl mb-1">
            Make a list of topics that you love or your favorite hobbies. You might love movies, sports, food, travel,
            art,
            or fashion. The subjects can be anything you want, as long as they interest and inspire you. Then, choose
            one
            item from the list and create a diary entry about it.
            <ul class="list-disc my-1 list-inside">
                <li class="mb-1 text-lg">For example, if you love sports, write about why you love a certain sport, your
                    favourite teams, and
                    personal goals you have if you play any sports yourself.</li>
                <li class="mb-1 text-lg">
                    If you love painting, you could write about your favorite painters, the painting styles that speak
                    to
                    you the most, recent paintings you've made, and ideas for future paintings.
                </li>
            </ul>
        </p>
        <h2 class="text-3xl py-2  font-semibold">
            Write the date in the corner or on the first line.
        </h2>
        <p class="text-xl mb-1">
            You may not write in your diary every day, so dating your entries can help you keep track of when things
            happened. Since you'll be writing in your diary over a long period of time, dates will also help you stay
            organized and provide context when you look back on your entries in the future.
            <ul class="list-disc my-1 list-inside">
                <li class="mb-1 text-lg">If you like, you can also put the time, day of the week, and your location
                    alongside the date.</li>
            </ul>
        </p>
        <h2 class="text-3xl py-2  font-semibold">
            Begin each entry with a topic in mind.
        </h2>
        <p class="text-xl mb-1">
            Most people reach for their diary when they have something they want to get down on paper or think about.
            This
            could be anything--something that happened that day, a dream you had, future plans, an event, an idea, or
            strong
            emotion or mood you're feeling.
            <ul class="list-disc my-1 list-inside">
                <li class="mb-1 text-lg">Once you start writing, you're free to veer off into any subject you like! But
                    having something in mind
                    when you start the entry can help kickstart the writing process.
                </li>
            </ul>
        </p>
        <h2 class="text-3xl py-2  font-semibold">
            Open with "Dear Diary" if you want to.
        </h2>
        <p class="text-xl mb-1">
            This is a completely personal choice, so go with what works best and feels right for you. At first,
            addressing
            the "Diary" can feel almost like reaching out to a friend rather than simply writing or talking to yourself.
            You
            may find this helpful if you're new to journaling
        </p>
        <h2 class="text-3xl py-2  font-semibold">
            Write in the first person by using "I" statements.
        </h2>
        <p class="text-xl mb-1">
            Diaries are very personal items, and they typically work best when you speak in the first person. In your
            diary,
            you are free to make everything about you! Many people find this aspect extremely cathartic, especially when
            it
            comes to exploring personal thoughts, emotions, and reactions.
            <ul class="list-disc my-1 list-inside">
                <li class="mb-1 text-lg">For example, you might write something like, "I'm worried about volleyball
                    tryouts this week. I've
                    practiced a lot and I feel ready, but I'm so nervous I can hardly eat."</li>
            </ul>
        </p>
        <h2 class="text-3xl py-2  font-semibold">
            Be honest in your entries.
        </h2>
        <p class="text-xl mb-1">
            Many people find diary writing cathartic because they can let go of inhibitions on the page and truly be
            themselves. Feel free to record your emotions, both positive and negative, in their entirety in your diary.
            Remember that no one will ever see what you've written, so you can write honestly about anything. This is
            for
            your eyes only.
            <ul class="list-disc my-1 list-inside">
                <li class="mb-1 text-lg">For example, you might write, "I feel jealous of Shaun's new car. I'm happy for
                    him, but it seems really
                    unfair that his parents bought him a brand new car. I'm working every day after school just to save
                    up
                    for a used car."
                </li>
                <li class="mb-1 text-lg">If you're afraid someone will find and read your diary, there are things you
                    can do to prevent this.
                    Physical diaries with locks and password-protected digital diaries are two of the most popular ways
                    to
                    control privacy.</li>
                <li class="mb-1 text-lg">Many people have epiphanies about themselves and their relationships through
                    honest diary writing. Be
                    open to learning about yourself as you write.</li>
            </ul>
        </p>
        <h2 class="text-3xl py-2  font-semibold">
            Don't worry too much about grammar and spelling.
        </h2>
        <p class="text-xl mb-1">
            Your diary is a safe space for you to vent and share without the pressures of external judgment. Write
            freely
            and without inhibition. Being grammatically correct, proper spelling, and writing perfect phrases are not
            nearly
            as important as getting your thoughts and feelings down on the page. Jot down the first things that come to
            mind
            when considering your day, current mood, and any feelings you're struggling with.
            <ul class="list-disc my-1 list-inside">

                <li class="mb-1 text-lg">For some people, it helps to take a few minutes at the beginning of each entry
                    to simply free write.
                </li>
            </ul>
        </p>
        <h2 class="text-3xl py-2  font-semibold">
            Use lots of details to preserve moments in time.
        </h2>
        <p class="text-xl mb-1">
            Diaries help you preserve thoughts and feelings as they're happening. You're also able to record events
            immediately after they happen when details are still fresh in your mind. Since memories can become
            unreliable,
            especially as time passes, recording precise details vividly can help you preserve moments in time exactly
            as
            they occurred.
            <ul class="list-disc my-1 list-inside">
                <li class="mb-1 text-lg">Detailed writing isn't for everyone, so don't feel like you have to write long,
                    wordy sentences. If you
                    find it easier to express your emotions in short bursts or even bullet points, feel free to do that.
                </li>
            </ul>
        </p>
        <h2 class="text-3xl py-2  font-semibold">
            Choose a specific time each day to write in your diary.
        </h2>
        <p class="text-xl mb-1">
            Many people struggle to find the time to write in their diary. Others simply forget to write. It helps to
            choose
            a specific time each day for diary writing so that you can develop the habit. Eventually, it will become
            second
            nature, but it can help to set a daily reminder on your phone until then!
            <ul class="list-disc my-1 list-inside">
                <li class="mb-1 text-lg">For example, you might choose to write in your diary every night right before
                    bed.</li>
                <li class="mb-1 text-lg">Don't set an unrealistic schedule for yourself. If writing every day seems out
                    of reach, plan on writing
                    entry 3 times a week instead.</li>
            </ul>
        </p>
        <h2 class="text-3xl py-2  font-semibold">
            Keep your writing sessions short in the beginning.
        </h2>
        <p class="text-xl mb-1">
            You don't have to set aside a huge chunk of time every day for writing in your diary! 10-15 minutes per
            session
            is a great goal when you're just starting out. Jot down your most pressing and immediate feelings and
            thoughts.
            You can always write more later in the week when you have time!
            <ul class="list-disc my-1 list-inside">
                <li class="mb-1 text-lg">For example, you could write a few key bullet points in your diary when you're
                    pressed for time.</li>
                <li class="mb-1 text-lg">Setting an intimidating schedule for yourself could be counterproductive. You
                    want journaling to be an
                    outlet, not a chore, so go easy on yourself.
                </li>
                <li class="mb-1 text-lg">Pick a time to write when you don't have other obligations or time constraints.
                </li>
            </ul>
        </p>
